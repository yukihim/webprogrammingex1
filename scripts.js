window.onload = function() {
  const selectCountry = document.getElementById("country"),
        selectProvince = document.getElementById("province"),
        selectDistrictCity = document.getElementById("district/city"),
        selectWardTownCommune = document.getElementById("ward/town/commune"),
        select = document.querySelectorAll("select")
        
        selectProvince.disabled = true,
        selectDistrictCity.disabled = true,
        selectWardTownCommune.disabled = true,

        select.forEach(select => {
          if(select.disabled == true) {
            select.style.cursor = "auto"
          }
        })

        for(let country in countryProvinceCityDistrictWard) {
          selectCountry.options[selectCountry.options.length] = new Option(country, country)
        }

        // Country change
        selectCountry.onchange = (e) => {
          selectProvince.disabled = false
          selectDistrictCity.disabled = true
          selectWardTownCommune.disabled = true

          selectProvince.length = 1
          selectDistrictCity.length = 1
          selectWardTownCommune.length = 1

          for (let province in countryProvinceCityDistrictWard[e.target.value]) {
            selectProvince.options[selectProvince.options.length] = new Option(province, province)
          }
        }

        // Province change
        selectProvince.onchange = (e) => {
          selectDistrictCity.disabled = false
          selectWardTownCommune.disabled = true

          selectDistrictCity.length = 1
          selectWardTownCommune.length = 1

          for (let districtCity in countryProvinceCityDistrictWard[selectCountry.value][e.target.value]) {
            selectDistrictCity.options[selectDistrictCity.options.length] = new Option(districtCity, districtCity)
          }
        }

        // District/City change
        selectDistrictCity.onchange = (e) => {
          selectWardTownCommune.disabled = false

          selectWardTownCommune.length = 1

          let wardTownCommune = countryProvinceCityDistrictWard[selectCountry.value][selectDistrictCity.value][e.target.value]

          for (let i = 0; i < wardTownCommune.length; ++i) {
            selectWardTownCommune.options[selectWardTownCommune.options.length] = new Option(wardTownCommune[i], wardTownCommune[i])
          }
        }
};